# Визначення класу Teacher
class Teacher:
    def __init__(self, first_name, last_name, age, email, can_teach_subjects):
        self.first_name = first_name
        self.last_name = last_name
        self.age = age
        self.email = email
        self.can_teach_subjects = set(can_teach_subjects)
        self.assigned_subjects = set()


def create_schedule(subjects, teachers):
    subjects_left = set(subjects)
    selected_teachers = []

    while subjects_left:
        top_teacher = None
        max_coverage = 0

        for teacher in teachers:
            coverage = len(teacher.can_teach_subjects & subjects_left)
            if coverage > max_coverage or (
                coverage == max_coverage
                and top_teacher
                and teacher.age < top_teacher.age
            ):
                max_coverage = coverage
                top_teacher = teacher

        if max_coverage == 0:
            return None

        assigned = top_teacher.can_teach_subjects & subjects_left
        top_teacher.assigned_subjects = assigned
        subjects_left -= assigned
        teachers.remove(top_teacher)
        selected_teachers.append(top_teacher)

    return selected_teachers


